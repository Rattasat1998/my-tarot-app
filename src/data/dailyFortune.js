// Get daily fortune based on date
export const getDailyFortune = () => {
    const today = new Date();
    const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24));
    return DAILY_FORTUNES[dayOfYear % DAILY_FORTUNES.length];
};

// Get daily card based on date
export const getDailyCard = (cards) => {
    const today = new Date();
    const seed = today.getFullYear() * 1000 + today.getMonth() * 100 + today.getDate();
    return cards[seed % cards.length];
};

// Stats (could be stored in localStorage for persistence)
export const getStats = () => {
    const stored = localStorage.getItem('tarot_stats');
    if (stored) {
        return JSON.parse(stored);
    }
    return {
        totalReadings: 1247,
        cardsRevealed: 8934,
        activeDays: 365
    };
};

export const incrementStats = (type) => {
    const stats = getStats();
    if (type === 'reading') stats.totalReadings++;
    if (type === 'card') stats.cardsRevealed++;
    localStorage.setItem('tarot_stats', JSON.stringify(stats));
    return stats;
};
