// Get monthly report seed based on zodiac + month + year
const getMonthlyReportSeed = (zodiacId, monthIndex) => {
    const year = new Date().getFullYear();
    const zodiacIndex = ZODIAC_SIGNS.findIndex(z => z.id === zodiacId);
    return ((year * 31 + monthIndex * 17 + zodiacIndex * 13) % 97) + 1;
};

// Get score (60-95) based on seed
const getMonthlyScore = (seed, offset = 0) => {
    return 60 + ((seed * 7 + offset * 11) % 36);
};

